# Bucles

# Bucle For
# Recorrer listas con for

numeros = [1,2,3,4,5,6]

# Primero, usamos la palabra clave reservada "for", luego asignamos un "alias" a cada uno de los elementos, dentro del elemento
# a iterar
for num in numeros:
    print(num)
    
#Range
for i in range(5):
    print(i)
    
contador = 0
# While
while contador < 10:
    print(contador)
    contador = contador + 1
    

    